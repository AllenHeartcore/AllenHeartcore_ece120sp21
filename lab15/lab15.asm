; <Main Function>
; This program accepts a series of ASCII characters, enlarges them one by one into 8*16-pixel images, 
; and prints the merged sentence onto the screen, 
; with characters representing black and white areas specified by the user. 
; x3000 - Starting address of the program
; x5000 - Test Data A: specified character representing white area
; x5001 - Test Data B: specified character representing black area
; x5002 - Test Data C: starting of the series of characters being enlarged and printed

; <Register Table>
; R0 [ADDR] - <Part 2   > PAP: Printing address pointer (for writing characters to be printed)
;             <Part 3   > PAH: Printing address holder (to be called by PUTS)
; R1 [DATA] - <Part 1&2A> CEP: Pointer of CBE
;                              (sometimes holding CBE: Character being examined)
;             <Part 2B  > CHR: Test Data A&B (see above, to be loaded in M[PAP])
; R2 [ADDR] - <Part 1   > CEP: Pointer of CBE
;             <Part 2   > FDP: Font data pointer
; R3 [DATA] -             FDH: Font data holder (row-wise)
; R4 [ITER] -             RLI: Row loop iterator
; R5 [ITER] -             HLI: Character loop iterator
; R6 [ITER] -             CLI: Column loop iterator



        .ORIG   x3000


                               ; <Part 1: Register Initialization>
         AND    R5,R5,#0       ; initialize HLI
         LD     R2,TDBADDR     ; prepare CEP for reading CBE (TDCADDR-1)
CHR_CNT  ADD    R2,R2,#1       ; increment CEP
         LDR    R1,R2,#0       ; read CBE
         BRz    CONTINIT       ; break the loop if a "NULL" is read
         ADD    R5,R5,#1       ; accumulate HLI
         BR     CHR_CNT        ; examine the next character
CONTINIT ST     R5,CHR_NUM     ; store the initial HLI (N) as a public variable
         LEA    R0,PRTSRCE     ; initialize PAP for writing characters


                               ; <Part 2: Image Loading>
         AND    R4,R4,#0       ; initialize RLI for 16 iterations (1)
         ADD    R4,R4,#15      ; initialize RLI for 16 iterations (2)
ROWLOOP  ADD    R4,R4,#0       ; set NZP with RLI
         BRn    DONE           ; break the loop if all the 16 rows are examined
         LD     R1,TDCADDR     ; initialize CEP (in <Part 2>)
         ADD    R4,R4,#-1      ; decrement RLI
         LD     R5,CHR_NUM     ; initialize HLI for N iterations (1)
         ADD    R5,R5,#-1      ; initialize HLI for N iterations (2)

CHRLOOP  ADD    R5,R5,#0       ; set NZP with HLI
         BRn    LF_PTIMG       ; break the loop if all the N characters are examined
         ST     R1,CE_TMP      ; temporarily store CEP (R1 usage: CEP -> CBE)
         LDR    R1,R1,#0       ; CBE calculation: R1 <- M[x5002+CHR_NUM-HLI-1] (ADDR -> DATA)
         ADD    R1,R1,R1       ; CBE*2
         ADD    R1,R1,R1       ; CBE*4
         ADD    R1,R1,R1       ; CBE*8
         ADD    R1,R1,R1       ; CBE*16, prepare for FDP calculation (1)
         LEA    R2,FONT_DATA   ; prepare for FDP calculation (2)
         ADD    R2,R1,R2       ; FDP calculation (1): FONT_DATA+CBE*16
         ADD    R2,R2,#15      ; FDP calculation (2): FONT_DATA+CBE*16+15 
         NOT    R3,R4          ; prepare -RLI-1 [temp. using R3]
         ADD    R2,R2,R3       ; FDP calculation (3): FONT_DATA+TDC*16+14-RLI
         LDR    R3,R2,#0       ; load FDH using the address in FDP
         LD     R1,CE_TMP      ; restore CEP (R1 usage: CBE -> CEP)
         ADD    R1,R1,#1       ; increment CEP
         ADD    R5,R5,#-1      ; decrement HLI
         AND    R6,R6,#0       ; initialize CLI for 8 iterations (1)
         ADD    R6,R6,#7       ; initialize CLI for 8 iterations (2)

COLLOOP  ADD    R6,R6,#0       ; set NZP with CLI
         BRn    CHRLOOP        ; break the loop if all the 8 columns are examined
         ADD    R3,R3,#0       ; set NZP with FDH
         ST     R1,CE_TMP      ; temporarily store CEP (R1 usage: CEP -> CHR)
         BRn    BLACK          ; fill in a character (CHR) representing black area (1)
         BRzp   WHITE          ; fill in a character (CHR) representing white area (1)
BLACK    LDI    R1,TDBADDR     ; load TDB (CHR as "black" character)
         BR     COLLP_C        ; break to column loop - continued
WHITE    LDI    R1,TDAADDR     ; load TDA (CHR as "white" character)
         BR     COLLP_C        ; break to column loop - continued
COLLP_C  STR    R1,R0,#0       ; fill in CHR at PAP (2)
         LD     R1,CE_TMP      ; restore CEP (R1 usage: CHR -> CEP)
         ADD    R3,R3,R3       ; FDH*2, left shift 1 digit to examine the next bit
         ADD    R0,R0,#1       ; increment PAP
         ADD    R6,R6,#-1      ; decrement CLI
         BR     COLLOOP        ; break for another judge on CLI


                               ; <Part 3: Line Feed & Print Image>
LF_PTIMG AND    R6,R6,#0       ; write Line Feed (x0A) in R6 for temporary use (1)
         ADD    R6,R6,#10      ; write Line Feed (x0A) in R6 for temporary use (2)
         STR    R6,R0,#0       ; fill in a Line Feed character at PAP
         ADD    R2,R2,#1       ; increment FDP
         LEA    R0,PRTSRCE     ; set PAH (printing address) (R0 usage: PAP -> PAH)
                               ; also, initialize PAP for writing another row of characters
         PUTS                  ; print a series of enlarged character
         BR     ROWLOOP        ; break for another judge on RLI
DONE     HALT                  ; halt execution


                               ; <Part 4: Post-program Data Storage>
TDAADDR .FILL   x5000          ; address of Test Data A
TDBADDR .FILL   x5001          ; address of Test Data B
TDCADDR .FILL   x5002          ; address of Test Data C
CHR_NUM .FILL   #0             ; address of holding the total number of characters
CE_TMP  .FILL   #0             ; temporary storage of R1 (CBE/CEP) value in <Part 2>
PRTSRCE .BLKW   #200           ; Print Source: leave blank spaces for characters to be printed



