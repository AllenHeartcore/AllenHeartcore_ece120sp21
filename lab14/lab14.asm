; Written by Chen, Ziyuan on Monday, May 10, 2021

; <Main Function>
; This program accepts a ASCII character and prints a 8*16-pixel enlarged image of it, 
; with characters representing black and white areas specified by the user. 
; x3000 - Starting address of the program
; x5000 - Test Data A: specified character representing white area
; x5001 - Test Data B: specified character representing black area
; x5002 - Test Data C: character being enlarged and printed

; <Register Table>
; R0 - [CHANGEABLE]
;      <Part 2> RLI: Row loop iterator
;      <Part 3> PAH: Printing address holder (to be called by PUTS)
; R1 -          TDA: Test Data A (constant, see above)
; R2 -          TDB: Test Data B (constant, lsee above)
; R3 - [CHANGEABLE]
;      <Part 1> TDC: Test Data C (see above)
;      <Part 2> CLI: Column loop iterator
; R4 -          FDP: Font data pointer
; R5 -          FDH: Font data holder (row-wise)
; R6 -          PAP: Printing address pointer (for writing characters to be printed)



        .ORIG   x3000


                               ; <Part 1: Register Initialization>
         LDI    R1,TDAADDR     ; load TDA
         LDI    R2,TDBADDR     ; load TDB
         LDI    R3,TDCADDR     ; load TDC
         ADD    R3,R3,R3       ; TDC*2
         ADD    R3,R3,R3       ; TDC*4
         ADD    R3,R3,R3       ; TDC*8
         ADD    R3,R3,R3       ; TDC*16, prepare for FDP calculation (1)
         LEA    R4,FONT_DATA   ; prepare for FDP calculation (2)
         ADD    R4,R3,R4       ; calculate FDP for the specified ADCII character
         LEA    R6,PRTSRCE     ; initialize PAP for writing characters


                               ; <Part 2: Image Loading>
         AND    R0,R0,#0       ; initialize RLI for 16 iterations (1)
         ADD    R0,R0,#15      ; initialize RLI for 16 iterations (2)
ROWLOOP  ADD    R0,R0,#0       ; set NZP with RLI
         BRn    PRINT          ; break the loop if all the 16 rows are examined
         LDR    R5,R4,#0       ; load FDH using the address in FDP
         ADD    R4,R4,#0       ; increment FDP
         ADD    R0,R0,#-1      ; decrement RLI
         AND    R3,R3,#0       ; initialize CLI for 7 iterations (1)
         ADD    R3,R3,#7       ; initialize CLI for 7 iterations (2)

COLLOOP  ADD    R3,R3,#0       ; set NZP with CLI
         BRn    LINEFEED       ; break the loop if all the 8 columns are examined
         ADD    R5,R5,#0       ; set NZP with FDH
         BRn    BLACK          ; fill in a character representing black area (1)
         BRzp   WHITE          ; fill in a character representing white area (1)
BLACK    STR    R2,R6,#0       ; fill in a character representing black area at PAP (2)
         BR     COLLP_C        ; break to column loop - continued
WHITE    STR    R1,R6,#0       ; fill in a character representing white area at PAP (2)
         BR     COLLP_C        ; break to column loop - continued
COLLP_C  ADD    R5,R5,R5       ; FDH*2, left shift 1 digit to examine the next bit
         ADD    R6,R6,#1       ; increment PAP
         ADD    R3,R3,#-1      ; decrement CLI
         BR     COLLOOP        ; break for another judge on CLI

LINEFEED AND    R3,R3,#0       ; write Line Feed (x0A) in R3 for temporary use (1)
         ADD    R3,R3,#10      ; write Line Feed (x0A) in R3 for temporary use (2)
         STR    R3,R6,#0       ; fill in a Line Feed character at PAP
         ADD    R4,R4,#1       ; increment FDP
         ADD    R6,R6,#1       ; increment PAP
         BR     ROWLOOP        ; break for another judge on RLI


                               ; <Part 3: Image Printing>
PRINT    LEA    R0,PRTSRCE     ; set PAH (printing address) (R0 usage: RLI -> PAH)
         PUTS                  ; print a 8*16 image of enlarged character
         HALT                  ; halt execution


                               ; <Part 4: Post-program Data Storage>
TDAADDR .FILL   x5000          ; address of Test Data A
TDBADDR .FILL   x5001          ; address of Test Data B
TDCADDR .FILL   x5002          ; address of Test Data C
PRTSRCE .BLKW   #160           ; Print Source: leave blank spaces for characters to be printed



